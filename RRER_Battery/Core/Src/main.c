/* Use UART to check Emergency button */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART2) {
    if (atoi(msg_buffer) == 9) {
      printf("ES received\r\n");
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);
      HAL_UART_Receive_IT(&huart2, msg_buffer, 1);
    } else if (atoi(msg_buffer) == 0) {
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_SET);
      HAL_UART_Receive_IT(&huart2, msg_buffer, 1);
    }
  }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_PIN) {
  if (GPIO_PIN == GPIO_PIN_9) {
    if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_9) == GPIO_PIN_SET) {
//      do when power turned on
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
      printf("Power turned ON\r\n");
      isON = 1;
    } else {
//      do when power turned off
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      printf("Power turned OFF\r\n");
      isON = 0;
    }
  }
}

